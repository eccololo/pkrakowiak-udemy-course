# Wykorzystując odpowiednią funkcję sprawdź typ podanych obiektów:
#
# (1, 2, 3)
#
# {1, 2, 3}
#
# [1, 2, 3]
#
# {1: 1, 2: 2, 3: 3}
#
# '123'
#
# 123
#
# W odpowiedzi wydrukuj każdy typ do konsoli w osobnej linii tak jak pokazano poniżej.
#

print(type((1, 2, 3)))

print(type({1, 2, 3}))

print(type([1, 2, 3]))

print(type({1: 1, 2: 2, 3: 3}))

print(type('123'))

print(type(123))